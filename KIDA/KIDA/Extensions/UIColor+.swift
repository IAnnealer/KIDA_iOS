import Foundation
import UIKit

extension UIColor {
    static func KIDA_orange() -> UIColor {
        return .init(red: 0.965, green: 0.404, blue: 0.02, alpha: 1)
    }
    
    static func KIDA_background() -> UIColor {
        return .init(red: 0.145, green: 0.145, blue: 0.145, alpha: 1)
    }

    static func KIDA_background2() -> UIColor {
        return .init(red: 0.23, green: 0.23, blue: 0.23, alpha: 1)
    }

    static func KIDA_background3() -> UIColor {
        return .init(red: 0.3, green: 0.3, blue: 0.3, alpha: 1)
    }
    
    static func KIDA_background4() -> UIColor {
        return .init(red: 0.48, green: 0.48, blue: 0.48, alpha: 1)
    }

    static func KIDA_dimmed() -> UIColor {
        return .init(red: 0, green: 0, blue: 0, alpha: 0.8)
    }
}

